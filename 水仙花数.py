"""
水仙花数
三位数   >>>>>>>  个位，十位，百位的三次方之和等于原来的那个三位数
"""

i = 100
while i < 1000:
    a = i // 100       # 取百位，用整除100
    b = i % 100 // 10  # 取十位，先用100取余拿到十位和个位，再用整除10拿到十位
    c = i % 10         # 取个位，直接用10 取余拿到个位
    if i == a ** 3 + b ** 3 + c ** 3:
        print(f"水仙花数{i}")
    i += 1



